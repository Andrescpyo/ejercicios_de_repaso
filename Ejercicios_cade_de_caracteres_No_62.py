#62. Escribir un programa que compare dos cadenas de caracteres y nos diga si son idénticos o no. 

cadena1 = input("\nIntroduce la primera cadena de caracteres: ")
cadena2 = input("Introduce la segunda cadena de caracteres: ")

# Comparamos las dos cadenas de caracteres.
if cadena1 == cadena2:
    print("\nLas dos cadenas de caracteres son idénticas.")
else:
    print("\nLas dos cadenas de caracteres no son idénticas.")